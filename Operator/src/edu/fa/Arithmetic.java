package edu.fa;

public class Arithmetic {
	public static void main(String[] args) {
		int a = 15;
		int b = 6;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a);
	}
}
